#!/bin/bash

########################################################
# usage:
#
# 	1. exec in serial
#
#		bssh.sh "rm -r xxx.log" 0
#
# 	2. exec in background
#		
#		bssh.sh "bash xxx.sh" 1
#
########################################################

########################################################
# Fill <ip_list> with your machine ip <ifconfig eth0>
########################################################
declare -a ip_list=(
172.21.16.28
172.21.16.31
172.21.16.42
172.21.16.22
172.21.16.30
172.21.16.49
172.21.16.33
172.21.16.47
172.21.16.37
172.21.16.24
172.21.16.26
172.21.16.12
172.21.16.13
172.21.16.10
172.21.16.2
172.21.16.8
172.21.16.3
172.21.16.16
172.21.16.15
)

cmd=${1:-date}
bg=${2:-0}

for i in ${ip_list[@]}
do
	echo; echo
	echo "Running [ $cmd ] on server [ $i ] in [ background=$bg ]"
	if [ $bg = "0" ]; then
		ssh $i "$cmd"
	else
		ssh $i "$cmd" &
	fi
done

